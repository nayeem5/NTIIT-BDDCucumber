package NTIIT.ProjectOct2019;

public class JavaLec4ForLoop {

	public static void main(String[] args) {
		int[] array1= {2,4,6,8};
		
		for(int index=0; index<4 ; index++) //for(init- initialise variable ; check expression; increment)
		{
			System.out.println(array1[index]);
		}

	}

}
